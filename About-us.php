		 <b>The University of Delhi</b> is the premier university of the country and is known for its high  standards in teaching and research and attracts  eminent scholars to  its faculty. It  was  established in  1922 as a unitary, teaching and residential  university by an Act of the then Central Legislative  Assembly.   The President of India is the Visitor,  the Vice  President is  the Chancellor  and  the  Chief Justice  of the Supreme Court of India is the Pro-Chancellor of the University.<br><br>

Ever since its  inception, a strong  commitment to  excellence in  teaching and research has  made the University of Delhi a role-model and path-setter for other universities in the country. Its rich academic tradition has  always attracted  the most  talented students  who later on  went on to make  important contributions to their society.<br><br>

When the  University  took birth, only  three  colleges  existed  in Delhi then:  St.  Stephen’s   College founded  in 1881,  Hindu College founded in 1899 and Ramjas College  founded  in  1917, which were subsequently affiliated to it. The University thus had a modest beginning with just three colleges, two faculties (Arts and Science) and about 750 students. In October 1933, the University   offices and the Library shifted to the Viceregal Lodge Estate, and till today this site houses the offices of the main functionaries of the University. The University has grown into one of the largest universities in India. At present, there are 16 faculties, 86 academic departments, 77   colleges  and 5  other  recognised   institutes  spread all over the city, with 132435 regular  students   (UG: 114494,PG:17941) and 261169 students (UG:258831,PG:2338) in non-formal education programme.<br><br>

Sir Maurice Gwyer,  the then Vice-Chancellor, realizing  the  importance  of a  distinguished  faculty to  act as role models,  relentlessly searched for talent all over  the  country  and roped in men of eminence to this University, such as Prof. D.S. Kothari in Physics, Prof. T.R. Sheshadri in Chemistry, Prof. P Maheshwari in Botany and Prof. M L Bhatia in Zoology.<br><br>

<b>Five Departments namely Chemistry, Geology, Zoology, Sociology and History have been awarded the status of the Centres of Advanced Studies.</b> These Centres of Advanced Studies have carved a niche for themselves as centres of excellence in teaching and research in their respective areas. In addition, a good  number  of University  departments are  also receiving   grants under   the  Special   Assistance Programme of the UGC in recognition of their outstanding academic work. 10 Departments (Germanic & Romance Studies, Hindi, Persian, Geography, Music, East Asian Studies, Anthropology, Mathematics, B.R.Ambedkar, M. I.L.) are getting grants under DRS, 2 Departments (Buddhist Studies, English) are getting grants under DSA, 3 Departments (English, Buddhist Studies, Social Work) are getting grants under AISHSS and 3 Departments (African Studies, East Asian Studies, Developing Countries Research Centre) are getting grants under Area Studies Programmes. Department of Adult, Continuing Education and Extension and Women’s Studies & Development Centre of the University are also getting special funding from UGC. The University today boasts of as many as 15 big libraries apart from libraries in colleges. The University Science Instrumentation Centre (USIC) which is now situated close to the Physics and Chemistry Departments houses a number of sophisticated and high-end research instruments. These instruments are used quite frequently by Teachers and Research Scholars of postgraduate departments of the University as well as by many other institutions in Delhi and its neighbourhood. The University has recently laid fibre-optic network in the North and the South Campuses connecting all colleges and departments.<br><br>

When the University of Delhi expanded in many directions to keep pace with a rapidly growing city, South Campus was established in 1973 to facilitate access for the residents of South Delhi. It moved to its present location on Benito Juarez Road, near Dhaula Kuan, in 1984. The Campus is now spread across 69 acres of green, hilly terrain and its buildings blend attractively with the natural surroundings. The various departments are located in the Faculty of Arts and the Faculty of Inter-disciplinary and Applied Sciences. S.P. Jain Centre for Management Studies is also located at the South Delhi Campus. Besides these, the Campus has a good library, a Health Centre, a Bank, a Post Office, DTC Pass Section and administrative and examination blocks. South Campus also provides some residential quarters for faculty members and the non-teaching staff. Outstation students are offered accommodation in three hostels.<br><br>
